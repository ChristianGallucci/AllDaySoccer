			<u><h4 style="padding-top:3%;">TEAM RULES</h4></u>
			<ul>
				<li>Minimum of 3 players required to start match. Teams with less than 3 players will be scored as a forfeit and the opposing team will be awarded the points for the win (3 points) and recorded as a 10-0 win.</li>
				<li>Each team consist of five players (or six, see note on female players below): four (or five with two female) field players and a goalkeeper</li>
				<li>A team is allowed two female field players instead of a single male player. Teams are allowed to play a single female if they so decide. If the goalkeeper is a female, she does not count towards additional female allowed. </li>
			</ul>
			<u><h4 style="padding-top:3%;">FIFA RULES</h4></u>
			<center><p>Apart from substitution, offside, or other rules listed below, referees will enforce matches using FIFA rules when applicable. </p></center>
			<u><h4 style="padding-top:3%;">MATCH DURATION</h4></u>
			<ul>
				<li>All adult league matches are 50 minutes in duration, with 25 minutes halves. There will be a half-time break, length of the break at the referee discretion.</li>
				<li>Matches start promptly according to league schedule. Time is maintained by a time clock, maintained by the referee. </li>
				<li>Failure to field a team of a minimum of three players (3) within 15 minutes of a team’s match time will result in a team forfeit resulting in a 10-0 loss recorded. If neither team is able to field the minimum required players, no points will be awarded to either team. </li>
				<li>Substitutions will be on the fly. Referee does not need to stop game or acknowledge substitution, except when there is a goalkeeper change. Substitution is unlimited.</li>
				<li>There is no offside play</li>
				<li>No throw-ins. Ball is marked at the spot where ball went out and “kicked” in.</li>
				<li>Ball cannot be thrown over the half line. Infraction of this rule will result in the ball being placed at the half line and given to the opposing team. Balls are allowed to be kicked over the half line.</li>
				<li>On a goal kick restart, ball must be kicked. Throwing is not allowed in this situation.</li>
				<li>If requested from the referee, opposing team must stand 10 yards away from where the out or infraction occurred.</li>
				<li>No double-teaming of players on the walls. IT IS A SAFETY ISSUE!</li>
				<li>A power play occurs when the referee gives a two-minute penalty to a player. The player who receives a two-minute penalty will have to remain on their bench until the referee signals that the two-minute penalty has ended. The player may not be replaced, the team must play with fewer players on the field. </li>
				<li>If the opposing team scores a goal during a power play the two-minute penalty is terminated – player may enter the match again. </li>
				<li>If players on both teams are issued a two-minute penalty, both players must sit out the full two-minute duration regardless if a goal is scored and may not enter the field of play until the referee signals them on.</li>
				<li>Goal kicks must be taken within the box.</li>
				<li>Corner kicks must be placed on designated line. </li>
				<li>The goal keeper can pick up the ball passed back to him by his teammates.</li>
				<li>No sliding or slide tackling.  The goalkeeper is allowed to slide only within the goal box.</li>
				<li>A penalty kick is awarded against a team that commits a direct kick foul inside the goal box.  Penalty kicks are taken from the top of the penalty arc.  All players, except for the goalkeeper and the kicker, must stand 5 yards behind the goal box at the time of the kick.</li>
				<li>All restarts are direct, including at kickoff, and at restart, after a goal is scored. </li>
			</ul>
			<u><h4 style="padding-top:3%;">FORFEITED MATCHES</h4></u>
