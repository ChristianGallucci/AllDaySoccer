			<ul>
			<li>League playoffs are scheduled based on league standings comprised by the league point system.</li>
			<li>Playoffs are single elimination; typically, the duration of each playoff game is similar to a regular season game. Organizers have the discretion to change game times to accommodate playoff formats or time availability.</li>
			<li>In the event the game is tied at the end of regulation, a 10-minute overtime period will be added after regulation time. The overtime period is sudden death; first team to score wins.</li>
			<li>If a goal has not been scored during the 10-minute overtime period, penalty kicks will determine the winner. Penalty kick will be sudden death.</li>
			<li>Players must have participated in at least one regular season game with his/her team to be eligible to play with them in the playoffs. No guest players are allowed.</li>
			</ul>
