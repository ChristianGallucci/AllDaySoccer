			<li>Teams who have less than the minimum number of players required at the start of match will forfeit their match.</li>
			<li>A team(s) can invite guest players (even from other team teams in the league) and the result will count once they are able to field the minimum required player count from their own team. This is only valid in regular season play.</li>
			<li>No rematches or makeups will be permitted.</li>
			</ul>
			<u><h4 style="padding-top:3%;">FOULS &amp; PENALTIES</h4></u>
