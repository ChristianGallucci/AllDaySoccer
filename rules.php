			<ul>
				<li>The referee will have sole jurisdiction over the conduct of the match.</li>
				<li>If a referee terminates a match for any reason, the score at the time of the termination will stand.</li>
				<li>No protests will be considered or permitted.</li>
			</ul>
			<u><h4 style="padding-top:3%;">PLAYER RULES</h4></u>
			<ul>
				<li>Absolutely no fighting! Law enforcement may be notified and reported. No abuse to referees, players or organizers - verbal or otherwise.  Any indication of such behavior by a player or spectator will result in immediate removal from the premises</li>
				<li>No foul language, or inappropriate or vulgar language from player or fans </li>
				<li>No smoking or alcoholic beverages are permitted on the premises.</li>
				<li>Continuous verbal abuse to a referee or any player will result in a 2-minute penalty in favor for the opposing team. </li>
				<li>Shin guards are recommended.</li>
				<li>No cleats or outdoor studs. </li>
				<li>No spitting, alcohol consumption or smoking is permitted. </li>
				<li>Players are considered eligible for play if they are documented on a team roster and signed a waiver. Minimum player age is 16 years determined on the start date of each season.</li>
				<li>Players under the age of 18 are required to have written consent by a parent and/or a legal guardian to be permitted to play in an adult league</li>
