			<li>Yellow Card = Two Minute Penalty.</li>
			<li>Two Yellow Cards = Player Ejection from Match.</li>
		    <li>Red Card = Player Ejection from Match &amp; Minimum of 1 Additional Match.</li>
 			<li>2 Red Cards in one season = Suspended for the remainder of the season.</li>
			</ul>
			<u><h4 style="padding-top:3%;">POINT SYSTEM </h4></u>
			<ul>
			<p>Teams are awarded: 3 points for a win, 1 point for a tie, 0 points for a loss. The league standings and tiebreakers are determined by the following:</p>
			<li>Total Team Points</li>
			<li>Head to Head Competition</li>
			<li>Goal Differential</li>
			<li>Goals For</li>
			<li>Goals Against</li>
			</br>
			<p>In the event of a 3-way tie, Head to Head is thrown out and tie breaker starts with Goal differentials.</p>
			<p>All scores, results and standings will be posted on website, or emailed to captains if website not available.</p>
			</ul>
